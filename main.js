function showMeal(meal) {

    document.getElementById('mealName').innerHTML = meal.name;
    document.getElementById('mealDescribtion').innerHTML = meal.describtion;
    document.getElementById('mealImg').src = meal.image
    document.getElementById('mealReviews').innerHTML = meal.reviews
    document.getElementById('mealRating').innerHTML = meal.rating
    document.getElementById('mealLeavel').innerHTML = meal.level
    document.getElementById('mealCuisine').innerHTML = meal.cuisine
    document.getElementById('prepTime').innerHTML = meal.prepTime + " min"
    document.getElementById('cookTime').innerHTML = meal.cookTime + " min"
    document.getElementById('servings').innerHTML = meal.Servings + " people"
    var ingredients = "";
    var instructions = "";
    var tips = "";
    for (var i = 0; i < meal.ingredients.length; i++) {

        ingredients += `
        
        <li class="d-flex gap-3">
          <span>${(i + 1)}</span>
          <p>${meal.ingredients[i]}</p>
        </li>
        `
    }
    document.getElementById('ingredientsList').innerHTML = ingredients;


    for (var i = 0; i < meal.instructions.length; i++) {

        instructions += `
        
        <li class="d-flex align-items-center gap-3 mb-4">
           <span>${(i + 1)}</span>
           <p>${meal.instructions[i]}</p>
        </li>
        `
    }
    document.getElementById('instructionsList').innerHTML = instructions;

    document.getElementById('calories').innerHTML = meal.nutrition.calories;
    document.getElementById('protein').innerHTML = meal.nutrition.Protein;
    document.getElementById('carbohydrates').innerHTML = meal.nutrition.carbohydrates;
    document.getElementById('fat').innerHTML = meal.nutrition.fat;
    document.getElementById('fiber').innerHTML = meal.nutrition.fiber;
    document.getElementById('sodium').innerHTML = meal.nutrition.Sodium;


    for (var i = 0; i < meal.tips.length; i++) {

        tips += `
        
        <div class="tips-item d-flex">
            <i class="fa-solid fa-circle-check"></i>
            <p class="mt-0">${meal.tips[i]}</p>
        </div>
        `
    }

    document.getElementById('pills-tips').innerHTML = tips;

    if (meal.prepTime + meal.cookTime >= 45) {

        document.getElementById('time-warning-display').innerHTML = `
    
   <div class="time-warning d-flex align-items-center rounded-3 d-block">
        <i class="fa-solid fa-triangle-exclamation  me-2"></i>
        <div>
            <span>Extended Preparation Time</span>
            <p class="mb-0">This recipe requires more than 45 minutes to prepare. Plan
            accordingly!</p>
        </div>
    </div>
    
    `;


    }
    else {

        document.getElementById('time-warning-display').innerHTML = `
    
   <div class="time-warning d-flex align-items-center rounded-3 d-none">
        <i class="fa-solid fa-triangle-exclamation  me-2"></i>
        <div>
            <span>Extended Preparation Time</span>
            <p class="mb-0">This recipe requires more than 45 minutes to prepare. Plan
            accordingly!</p>
        </div>
    </div>
    
    `;

    }

}

var lastRandomIndex;
function generateRandomeIndex() {
    var randomIndex;

    do {
        randomIndex = Math.floor(Math.random() * meals.length);
    } while (lastRandomIndex == randomIndex);

    lastRandomIndex = randomIndex;

    showMeal(meals[randomIndex])
}


document.getElementById('showMealBtn').onclick = generateRandomeIndex;
window.onload = generateRandomeIndex;
